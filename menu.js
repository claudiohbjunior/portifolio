$(document).ready(function () {
    // Salvar a URL do vídeo quando o modal é aberto
    $('#videoModal').on('show.bs.modal', function () {
      var videoUrl = "https://www.youtube.com/embed/x6TxAwNv-mY?si=zQBnLAHpzyUlNb4h"; // Substitua pela sua URL do vídeo
      $('.youtube-video').attr('src', videoUrl);
    });

    // Pausar o vídeo quando o modal é fechado
    $('#videoModal').on('hidden.bs.modal', function () {
      $('.youtube-video').attr('src', '');
    });
  });




